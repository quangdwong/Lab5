    private String getMaGiai() {
        String selectedTenGiai = this.cb_TenGiai.getSelectedItem().toString();
        String maGiai = "";

        try {
            Connection conn = new orclConnection().getConnection();
            String SQL = "select mag from GIAIDAU where tengiai = ?";

            PreparedStatement ps = conn.prepareStatement(SQL);
            ps.setString(1, selectedTenGiai);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                maGiai = rs.getString(1);
            }

        } catch (Exception e) {
            System.out.println(e);
        }
        return maGiai;
    }

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed

        try {
            int x = 0;
            Connection conn = new orclConnection().getConnection();
            String SQL = "insert into ct_gd values (?, ?)";

            for (int i = 0; i < tbl_DanhSachDoiThamGia.getRowCount(); i++) {
                String maDoi = tbl_DanhSachDoiThamGia.getValueAt(i, 0).toString();

                PreparedStatement ps = conn.prepareStatement(SQL);
                ps.setString(1, getMaGiai());
                ps.setString(2, maDoi);

                x = ps.executeUpdate();

            }
            if (x == 1) {
                JOptionPane.showMessageDialog(this, "Them du lieu thanh cong");
            } else {
                JOptionPane.showMessageDialog(this, "Them du lieu that bai");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void tbl_DanhSachDoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_DanhSachDoiMouseClicked
        String selectedMaDoi = tbl_DanhSachDoi.getValueAt(tbl_DanhSachDoi.getSelectedRow(), 0).toString();
        String selectedTenDoi = tbl_DanhSachDoi.getValueAt(tbl_DanhSachDoi.getSelectedRow(), 1).toString();

        String[] row = {selectedMaDoi, selectedTenDoi};

        DefaultTableModel tm_DoiThamGia = (DefaultTableModel) tbl_DanhSachDoiThamGia.getModel();
        tm_DoiThamGia.addRow(row);

        DefaultTableModel tm_Doi = (DefaultTableModel) tbl_DanhSachDoi.getModel();
        tm_Doi.removeRow(tbl_DanhSachDoi.getSelectedRow());
    }//GEN-LAST:event_tbl_DanhSachDoiMouseClicked

