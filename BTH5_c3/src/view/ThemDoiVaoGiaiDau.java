    private DefaultTableModel getTableModel_ForDSDoi() {
        DefaultTableModel model = new DefaultTableModel();

        String[] title = {"Mã đội", "Tên đội"};
        model.setColumnIdentifiers(title);

        try {
            Connection conn = new orclConnection().getConnection();
            String SQL = "select mad, tendoi from DOIBONG";
            String[] row = new String[2];

            PreparedStatement ps = conn.prepareStatement(SQL);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                row[0] = rs.getString(1);
                row[1] = rs.getString(2);
                model.addRow(row);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

        return model;
    }

    private DefaultComboBoxModel getComboBoxModel() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        try {
            Connection conn = new orclConnection().getConnection();
            String SQL = "select tengiai from giaidau";
            ArrayList<String> tengiaiList = new ArrayList<>();

            PreparedStatement ps = conn.prepareStatement(SQL);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                tengiaiList.add(rs.getString(1));
            }

            model = new DefaultComboBoxModel(tengiaiList.toArray());
        } catch (Exception e) {
            System.out.println(e);
        }

        return model;
    }

